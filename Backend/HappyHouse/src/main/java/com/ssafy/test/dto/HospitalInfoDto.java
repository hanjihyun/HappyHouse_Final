	public String getBasicDate() {
		return basicDate;
	}
	public void setBasicDate(String basicDate) {
		this.basicDate = basicDate;
	}
	public String getSido() {
		return sido;
	}
	public void setSido(String sido) {
		this.sido = sido;
	}
